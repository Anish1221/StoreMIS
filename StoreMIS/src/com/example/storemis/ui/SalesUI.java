    private void btn_addSalesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addSalesActionPerformed
        // TODO add your handling code here:
        int customer_id = 0;
        String firstName = txt_fName.getText();
        String lastName = txt_lName.getText();
        String phone = txt_phone.getText();
        String address = txt_address.getText();
        int order_id = Integer.parseInt(txt_orderId.getText());

        if (firstName.length() == 0 || lastName.length() == 0) {
            JOptionPane.showMessageDialog(null, "Add first name and last name...");
        } else {
            try {
                //customer add or check
                CustomerDAO customerDAO = new CustomerDAOImpl();
                Customer customer = new Customer();

                customer.setFirstName(firstName);
                customer.setLastName(lastName);
                customer.setPhone(phone);;
                customer.setAddress(address);

                customer_id = customerDAO.getByName(firstName, lastName);
                //JOptionPane.showMessageDialog(null, customer_id);
                while (customer_id < 0) {
                    if (customerDAO.insert(customer) > 0) {
                        customer_id = customerDAO.getLastId();
                        //JOptionPane.showMessageDialog(null, customer_id);
                    }
                }

                //orders insert in database
                ordersDAO = new OrdersDAOImpl();
                Orders order = new Orders();

                order.setOrderId(order_id);
                order.setOrderCustomerId(customer_id);

                if (ordersDAO.insert(order) > 0) {
                    //JOptionPane.showMessageDialog(null, "Inserted in Orders");
                    model = (DefaultTableModel) tbl_sales.getModel();
                    OrdersInfoDAO ordersInfoDAO = new OrdersInfoDAOImpl();
                    OrdersInfo ordersInfo = new OrdersInfo();

                    //JOptionPane.showMessageDialog(null, model.getRowCount());
                    for (int i = 0; i < model.getRowCount(); i++) {
                        ProductsDAO productDAO = new ProductsDAOImpl();

                        int product_id = productDAO.getId(model.getValueAt(i, 0).toString());
                        int order_qty = Integer.parseInt(model.getValueAt(i, 1).toString());
                        double total = Double.parseDouble(model.getValueAt(i, 3).toString());

                        ordersInfo.setOrderId(order_id);
                        ordersInfo.setCustomerId(customer_id);
                        ordersInfo.setProductId(product_id);
                        ordersInfo.setOrderQty(order_qty);
                        ordersInfo.setTotal(total);

                        ordersInfoDAO.insert(ordersInfo);
                        //JOptionPane.showMessageDialog(null, "Inserted in OrdersInfo");

                        int qty = productDAO.getQty(product_id) - order_qty;
                        productDAO.deductQty(qty, product_id);
                        //JOptionPane.showMessageDialog(null, "Qty deducted");
                    }

                    JOptionPane.showMessageDialog(null, "Successful...");
                    btn_addSales.hide();

                } else {
                    JOptionPane.showMessageDialog(null, "Something went wrong...");

                }
            } catch (Exception e) {

            }
        }


    }//GEN-LAST:event_btn_addSalesActionPerformed

    public void grandTotal() {
        model = (DefaultTableModel) tbl_sales.getModel();
        double grandTotal = 0;
        for (int i = 0; i < model.getRowCount(); i++) {
            double total = Double.parseDouble(model.getValueAt(i, 3).toString());
            grandTotal += total;
        }
        txt_grandTotal.setText(Double.toString(grandTotal));
    }

