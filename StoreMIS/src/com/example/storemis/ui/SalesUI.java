    public void FillComboBox() {
        ProductsDAO productDAO = new ProductsDAOImpl();
        try {
            productDAO.getAll().forEach(p -> {
                cmb_productName.addItem(p.getProductName());
            });
        } catch (ClassNotFoundException | SQLException ce) {

        }
    }

    private void cmb_productNamePopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_cmb_productNamePopupMenuWillBecomeInvisible
        // TODO add your handling code here:
        ProductsDAO productDAO = new ProductsDAOImpl();
        String selected = (String) cmb_productName.getSelectedItem();
        try {
            productDAO.getByName(selected).forEach(p -> {
                txt_availableQty.setText(String.valueOf(p.getQuantity()));
                txt_unitPrice.setText(String.valueOf(p.getUnitPrice()));
            });
            txt_salesQty.setText("0");
            txt_total.setText("0");
        } catch (Exception e) {

        }

    }//GEN-LAST:event_cmb_productNamePopupMenuWillBecomeInvisible

